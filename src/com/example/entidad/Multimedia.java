package com.example.entidad;

public class Multimedia {
	private String idMultimedia;
	private String TituloMultimedia;
	private String DescripcionMultimedia;
	private String urlImagenMultimedia;
	private String generoMultimedia;

	public String getIdMultimedia() {
		return idMultimedia;
	}

	public void setIdMultimedia(String idMultimedia) {
		this.idMultimedia = idMultimedia;
	}

	public String getTituloMultimedia() {
		return TituloMultimedia;
	}

	public void setTituloMultimedia(String tituloMultimedia) {
		TituloMultimedia = tituloMultimedia;
	}

	public String getDescripcionMultimedia() {
		return DescripcionMultimedia;
	}

	public void setDescripcionMultimedia(String descripcionMultimedia) {
		DescripcionMultimedia = descripcionMultimedia;
	}

	public String getUrlImagenMultimedia() {
		return urlImagenMultimedia;
	}

	public void setUrlImagenMultimedia(String urlImagenMultimedia) {
		this.urlImagenMultimedia = urlImagenMultimedia;
	}

	public String getGeneroMultimedia() {
		return generoMultimedia;
	}

	public void setGeneroMultimedia(String generoMultimedia) {
		this.generoMultimedia = generoMultimedia;
	}

}
