package com.example.entidad;

public class Diario {
	private String id;
	private String fecha;
	private String mes;
	private String numeroVersiculo;
	private String versiculo;
	private String planLectura;
	private String autorPensamiento;
	private String pensamiento;
	private String idCualidad;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	public String getNumeroVersiculo() {
		return numeroVersiculo;
	}
	public void setNumeroVersiculo(String numeroVersiculo) {
		this.numeroVersiculo = numeroVersiculo;
	}
	public String getVersiculo() {
		return versiculo;
	}
	public void setVersiculo(String versiculo) {
		this.versiculo = versiculo;
	}
	public String getPlanLectura() {
		return planLectura;
	}
	public void setPlanLectura(String planLectura) {
		this.planLectura = planLectura;
	}
	public String getAutorPensamiento() {
		return autorPensamiento;
	}
	public void setAutorPensamiento(String autorPensamiento) {
		this.autorPensamiento = autorPensamiento;
	}
	public String getPensamiento() {
		return pensamiento;
	}
	public void setPensamiento(String pensamiento) {
		this.pensamiento = pensamiento;
	}
	public String getIdCualidad() {
		return idCualidad;
	}
	public void setIdCualidad(String idCualidad) {
		this.idCualidad = idCualidad;
	}
	
	
}
